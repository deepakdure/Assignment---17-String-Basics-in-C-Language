// 6. Write a program to reverse a string
#include <stdio.h>
int main()
{
    char str[50];
    int i, j;
    printf("enter a string = ");
    gets(str);
    for (i = 0; i < str[i]; i++)
        ;
    printf("Reverse String is = ");
    for (j > i; j >= 0; j--)
    {
        printf("%c", str[j]);
    }
    return 0;
}